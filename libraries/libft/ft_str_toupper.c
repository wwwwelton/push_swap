#include "libft.h"

int	ft_str_toupper(char *str)
{
	int	i;

	if (!str)
		return (-1);
	i = 0;
	while (*str)
	{
		if (*str >= 'a' && *str <= 'z')
		{
			*str -= 32;
			i++;
		}
		str++;
	}
	return (i);
}
